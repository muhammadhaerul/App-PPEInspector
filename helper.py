def detect_video(conf, model):
    source_video = st.sidebar.file_uploader(
        "Upload a video...", type=("mp4", "avi", "mov", "wmv", "flv", "mkv"))

    is_display_tracker, tracker = display_tracker_options()

    if source_video is not None:
        st.video(source_video)
        if st.sidebar.button('Detect Video Objects'):
            try:
                temp_file = tempfile.NamedTemporaryFile(delete=False)
                temp_file.write(source_video.read())
                vid_cap = cv2.VideoCapture(temp_file.name) 
                st_frame = st.empty()
                while vid_cap.isOpened():
                    success, image = vid_cap.read()
                    if success:
                        _display_detected_frames(conf, model, st_frame, image, is_display_tracker, tracker)
                    else:
                        vid_cap.release()
                        break
            except Exception as e:
                st.sidebar.error("Error loading video: " + str(e))


